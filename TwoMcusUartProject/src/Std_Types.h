#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char defined_uint8_t;
typedef unsigned short int defined_uint16_t;
typedef unsigned int defined_uint32_t;
typedef unsigned long long int defined_uint64_t;

typedef float defined_f32;
typedef double defined_f64;

typedef signed char defined_sint8_t;
typedef signed short int defined_sint16_t;
typedef signed int defined_sint32_t;
typedef signed long long int defined_sint64_t;

#endif /* STD_TYPES_H_ */
